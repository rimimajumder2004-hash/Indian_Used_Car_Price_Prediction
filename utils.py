import pandas as pd
import io
from fpdf import FPDF
from datetime import datetime


# ─────────────────────────── Formatting ───────────────────────────
def format_price_inr(price_in_lakhs: float, for_pdf: bool = False) -> str:
    """Format price in Indian Rupees (lakhs). Use ASCII symbol for PDF."""
    symbol = "Rs." if for_pdf else "₹"
    if price_in_lakhs >= 100:
        crores = price_in_lakhs / 100
        return f"{symbol}{crores:.2f} Cr"
    return f"{symbol}{price_in_lakhs:.2f} L"


# ─────────────────────────── CSV Export ───────────────────────────
def export_prediction_csv(predictions: list) -> bytes:
    """Export list of predictions as CSV bytes."""
    df = pd.DataFrame(predictions)
    buffer = io.StringIO()
    df.to_csv(buffer, index=False)
    return buffer.getvalue().encode("utf-8")


# ─────────────────────────── PDF Export ───────────────────────────
class PredictionPDF(FPDF):
    def header(self):
        # Header bar
        self.set_fill_color(102, 126, 234)
        self.rect(0, 0, 210, 30, "F")
        self.set_font("Helvetica", "B", 18)
        self.set_text_color(255, 255, 255)
        self.cell(0, 15, "", ln=True)
        self.cell(0, 12, "  India Used Car Price Prediction Report", ln=True)
        self.set_text_color(0, 0, 0)
        self.ln(5)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, f"Generated on {datetime.now().strftime('%d %b %Y %H:%M')} | Powered by Random Forest ML", align="C")

    def section_title(self, title):
        self.set_font("Helvetica", "B", 13)
        self.set_text_color(102, 126, 234)
        self.cell(0, 8, title, ln=True)
        self.set_draw_color(102, 126, 234)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(3)
        self.set_text_color(0, 0, 0)

    def key_value_row(self, key, value, bg=False):
        self.set_font("Helvetica", "", 11)
        if bg:
            self.set_fill_color(245, 247, 255)
        else:
            self.set_fill_color(255, 255, 255)
        self.set_font("Helvetica", "B", 10)
        self.cell(80, 8, f"  {key}", border=0, fill=True)
        self.set_font("Helvetica", "", 10)
        self.cell(0, 8, str(value), border=0, fill=True, ln=True)

    def price_box(self, price_str, low_str, high_str):
        # Price highlight box
        self.set_fill_color(102, 126, 234)
        self.set_text_color(255, 255, 255)
        self.set_font("Helvetica", "B", 22)
        self.ln(5)
        self.cell(0, 18, f"Estimated Price: {price_str}", align="C", fill=True, ln=True)
        self.set_font("Helvetica", "", 11)
        self.cell(0, 10, f"95% Confidence Range: {low_str}  to  {high_str}", align="C", fill=True, ln=True)
        self.set_text_color(0, 0, 0)
        self.ln(5)


def export_prediction_pdf(input_data: dict, price: float, low: float, high: float) -> bytes:
    """Generate a PDF report for the prediction."""
    pdf = PredictionPDF()
    pdf.add_page()

    # Price result
    pdf.section_title("Predicted Price")
    pdf.price_box(format_price_inr(price, for_pdf=True), format_price_inr(low, for_pdf=True), format_price_inr(high, for_pdf=True))

    # Car details
    pdf.section_title("Car Details Entered")
    for i, (key, val) in enumerate(input_data.items()):
        pdf.key_value_row(key.replace("_", " ").title(), val, bg=(i % 2 == 0))
    pdf.ln(5)

    # Disclaimer
    pdf.section_title("Notes")
    pdf.set_font("Helvetica", "I", 9)
    pdf.set_text_color(120, 120, 120)
    pdf.multi_cell(0, 6,
        "This prediction is generated by a Random Forest machine learning model trained on historical "
        "India used car data. Actual market prices may vary based on vehicle condition, location, "
        "negotiation, and market demand. This report is for informational purposes only.")

    return pdf.output()